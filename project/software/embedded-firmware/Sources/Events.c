/*
** ===================================================================
**     Event       :  Cap1_OnCapture (module Events)
**
**     Component   :  Cap1 [Capture]
**     Description :
**         This event is called on capturing of Timer/Counter actual
**         value (only when the component is enabled - <Enable> and the
**         events are enabled - <EnableEvent>.This event is available
**         only if a <interrupt service/event> is enabled.
**     Parameters  : None
**     Returns     : Nothing
** ===================================================================
*/
void Cap1_OnCapture(void)
{
  if(j==0){ //flanco de subida de señal 'echo' --> se activa cuandpo HC-SR04 envia 8 pulsos de ultrasonido
	  (void)Cap1_Reset(); // reinicia contador
	  j=1;
  }else{ // flanco de bajada de señal 'echo' --> cuando HC-SR04 detecta un pulso de regreso
	  (void)Cap1_GetCaptureValue(&time); // adquiere duracion (en us) de señal 'echo' 
	  (void)Cap1_Disable(); //desactiva contador
	  distance=time/116; // factor de conversion: tiempo (us) --> distance (cm)
	  j=2;
  }
}

